package com.tms.lesson1;

public class types {

    public static void main(String[] args) {

        //Приметивные типы

        //Целые
        byte b = 1;
        short s = 22;
        char c = 'a';
        int i = 128;
        long l = 33;

        //Дробные
        float f = 22.5F;
        double d = 36.7;

        int i1 = 15;
        int i2 = 10;
        double i3 = (double) i1 / (double) i2;
        System.out.println(i3);

        System.out.println(c+1);
        System.out.println((int)c);
        System.out.println(i);
        System.out.println((byte)i);

    }
}
